# loop  - to run a block of statements repeatedly
# while loop  -run a set of statements repeatedly until the condition becomes false
#Syntax
# while condition:
#   code
#   inc/dec operator
a=0
while a<=10:
    print("hello")    # prints "hello"  11 times
    a+=1

print("\nwhile decrement example")
i=10
while i>0:
    print(i)
    i-=1