# Data Types  - the type of value stored...........str,int,float,double,bool,complex,mapping type

# str   - String (Non-numeric)
a="Luminar Technolab"
print(a)
print(type(a))

# int  - integer (numeric)
b=10
print(b)
print(type(b))

# float/double  - decimal point numbers (numeric)
c=0.2
print(c)
print(type(c))

# bool  - Boolean value (Non-numeric)
d=True
print(d)
print(type(d))

e=False
print(e)
print(type(e))