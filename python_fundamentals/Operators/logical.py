# Logical/Bitwise operations  :  &,|,^  .............converts the inputs into bits and perform operations

print(3&4)  # Logical AND  - gives 1 only if all the operands are 1
print(3|4)  # Logical OR   - gives 1 if atleast one of the operands is 1
print(3^4)  # Logical XOR  - gives 1 if either of the operands is 1
